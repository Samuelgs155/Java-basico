public class TablaMultiplicar5 {
	
	/**
	 * Desarrollar un programa que muestre la tabla de multiplicar del 5 (del 5 al 50) 
	 * @param args
	 */
	public static void main(String[] args) {
		
		for(int i=0; i <=10; i++) {
			System.out.println("5 * " + i + " = " + (5*i));
		}
		
	}

}
