package control;

public class Operadores_02 {

	public static void main(String[] args) {
		int numero = 10;
		
		System.out.println(numero);		
		numero = numero + 5;
		
		System.out.println(numero++);
		System.out.println(numero);
		
		numero += 5;
		System.out.println(numero);
	}
}
